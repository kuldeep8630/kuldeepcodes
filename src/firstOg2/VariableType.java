package firstOg2;

public class VariableType {

	public static void main(String[]args) {
		int a,b,sum,sub,multi,div;
		a=80;
		b=40;
		sum=a+b;
		sub=a-b;
		multi=a*b;
		div=a/b;
		System.out.println("add="+sum);
		System.out.println("sub="+sub);
		System.out.println("multi="+multi);
		System.out.println("div="+div);
	}

}
